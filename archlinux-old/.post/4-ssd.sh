#!/usr/bin/env bash

# ==============================================================================
#                               PARAMETERS
# ==============================================================================
# This script sets up fstrim for SSD maintenance and performance.

# ==============================================================================
#                              INSTALL UTIL-LINUX
# ==============================================================================
sudo pacman -S --noconfirm util-linux &> /dev/null

# ==============================================================================
#                           ENABLE FSTRIM TIMER
# ==============================================================================
sudo systemctl enable --now fstrim.timer &> /dev/null
